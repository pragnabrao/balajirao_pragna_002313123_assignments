    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        
        String FirstName = txtFirstName.getText();
        String LastName = txtLastName.getText();
        String SocialSecurityNumber = txtSSN.getText();
        String HStreetAddress = txtHStreetAddress.getText();
        String WStreet = txtWStreet.getText();
        String HState = txtHState.getText();
        String WState = txtWState.getText();
        String HCity = txtHCity.getText();
        String WCity = txtWCity.getText();
        
        int age = Integer.parseInt(txtAge.getText());
        int homeUnitNumber = Integer.parseInt(txtHUnitNumber.getText());
        int homeZipCode = Integer.parseInt(txtHZipCode.getText());
        int workUnitNumber = Integer.parseInt(txtWUnitNumber.getText());
        int workZipCode = Integer.parseInt(txtWZipCode1.getText());
        
        double height = Double.parseDouble(txtHeight.getText());
        float weight = Float.parseFloat(txtWeight.getText());
        
        
        if (FirstName.isBlank() || LastName.isBlank() || SocialSecurityNumber.isBlank() || HStreetAddress.isBlank() || WStreet.isBlank() || HState.isBlank() || WState.isBlank() || HCity.isBlank() || WCity.isBlank()) 
        {
            JOptionPane.showMessageDialog(null, "All fields are mandatory");
            return;
        }
        if (person != null) {
        person.setFirstName(FirstName);
        person.setLastName(LastName);
        person.setSocialSecurityNumber(SocialSecurityNumber);
        person.setHomeStreetAddress(HStreetAddress);
        person.setHomeCity(HCity);
        person.setHomeState(HState);
        person.setWorkStreetAddress(WStreet);
        person.setWorkCity(WCity);
        person.setWorkState(WState);
        person.setHeight(height);
        person.setWeight(weight);
        person.setHomeUnitNumber(homeUnitNumber);
        person.setWorkUnitNumer(workUnitNumber);
        person.setHomeZipCode(homeZipCode);
        person.setWorkZipCode(workZipCode);
        person.setAge(age);


        
        JOptionPane.showMessageDialog(null, "Account sucessfully updated!", "WARNING", JOptionPane.WARNING_MESSAGE);

        } else {
            JOptionPane.showMessageDialog(null, "No account selected. Please select an account first.", "Error", JOptionPane.ERROR_MESSAGE);
    }
        
    setViewMode();
    }//GEN-LAST:event_btnSaveActionPerformed


