    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        // TODO add your handling code here:
        
        // Remove the current panel from the container
        personManagementContainer.remove(this);

        // Get the last panel from the stack
        Component[] panelStack = personManagementContainer.getComponents();
        if (panelStack.length > 0) {
        JPanel lastPanel = (JPanel) panelStack[panelStack.length - 1];

            // Check if the last panel is of type ManagePeopleJPanel
            if (lastPanel instanceof ManagePeopleJPanel) {
            ManagePeopleJPanel managePeoplePanel = (ManagePeopleJPanel) lastPanel;
            managePeoplePanel.populateTable(); // Refresh data on the Manage panel
        } else if (lastPanel instanceof ListPeopleProfilesJPanel) {
            ListPeopleProfilesJPanel listPeoplePanel = (ListPeopleProfilesJPanel) lastPanel;
            
        }
    }

    // Navigate to the previous panel
    CardLayout layout = (CardLayout) personManagementContainer.getLayout();
    layout.previous(personManagementContainer);

                                      
       
        
    }//GEN-LAST:event_btnBackActionPerformed

